//traditional
function findOddOrEven(no){
    if(no%2==0){
        console.log("even");
    }else{
        console.log("odd");
        }
}
//using arrow function
const oddOrEven=(no)=>{
    if(no%2==0){
        consolelog("even");
    }else{
        console.log("odd");
    }
}
findOddOrEven(17);
oddOrEven(20);